CREATE OR REPLACE TRIGGER CALC_PKG_EXPIRED_DATETIME
BEFORE INSERT OR UPDATE ON PATIENT_PURCHASE_DETAILS
FOR EACH ROW
DECLARE
    v_validity_days NUMBER;
   	v_validity_type VARCHAR2(10);
   	v_validity_datetime DATE;
BEGIN
    -- Only calculate if PURCHASED_DATETIME is not null and PACKAGE_STATUS is updated to 'Purchased'
    IF :NEW.PURCHASED_DATETIME IS NOT NULL AND :NEW.PACKAGE_STATUS = 'Purchased' THEN
        -- Fetch PACKAGE_VALIDITY, PACKAGE_VALIDITY_TYPE, and PACKAGE_VALIDITY_DATETIME
        SELECT PACKAGE_VALIDITY, PACKAGE_VALIDITY_TYPE, PACKAGE_VALIDITY_DATETIME
        INTO v_validity_days, v_validity_type, v_validity_datetime
        FROM HOSPITAL_PACKAGE
        WHERE PACKAGE_ID = :NEW.PACKAGE_ID;
        
        -- Assign EXPIRED_DATETIME based on PACKAGE_VALIDITY_TYPE
        IF v_validity_type = 'Days' THEN
            -- Calculate the EXPIRED_DATETIME based on the latest PURCHASED_DATETIME
            :NEW.EXPIRED_DATETIME := :NEW.PURCHASED_DATETIME + v_validity_days;
        ELSIF v_validity_type = 'Date' THEN
            -- Set EXPIRED_DATETIME to PACKAGE_VALIDITY_DATETIME
            :NEW.EXPIRED_DATETIME := v_validity_datetime;
        END IF;
    END IF;
END; -- DONE EXECUTE

