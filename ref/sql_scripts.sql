CREATE SEQUENCE PACKAGE_PURCHASE_NO_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 999999
CYCLE;
   
CREATE OR REPLACE TRIGGER TRG_PURCHASE_NO
BEFORE INSERT ON PATIENT_PURCHASE_DETAILS
FOR EACH ROW
DECLARE
    running_number NUMBER;
BEGIN
    SELECT PACKAGE_PURCHASE_NO_SEQ.NEXTVAL INTO running_number FROM dual;
    :NEW.PACKAGE_PURCHASE_NO := 'PP' || TO_CHAR(SYSDATE, 'YY') || '-' || LPAD(running_number, 6, '0');
END;

CREATE SEQUENCE PAYMENT_REF_ID_SEQ;
CREATE OR REPLACE FUNCTION GENERATE_PAYMENT_REF_ID
RETURN VARCHAR2 AS
  v_year_part VARCHAR2(2);
  v_running_number NUMBER;
BEGIN
  -- Generate the year part (YY) based on the current year
  v_year_part := TO_CHAR(SYSDATE, 'YY');
  
  -- Get the next value from the sequence
  SELECT PAYMENT_REF_ID_SEQ.NEXTVAL INTO v_running_number FROM DUAL;
  
  -- Return the generated payment reference ID with the format MPYY-NNNNNN
  RETURN 'MP' || v_year_part || '-' || LPAD(v_running_number, 6, '0');
END;

CREATE OR REPLACE TRIGGER CALC_PKG_EXPIRED_DATETIME
BEFORE INSERT OR UPDATE ON PATIENT_PURCHASE_DETAILS
FOR EACH ROW
DECLARE
    v_validity_days NUMBER;
BEGIN
    -- Only calculate if PURCHASED_DATETIME is not null, PACKAGE_STATUS is updated to 'Purchased', and EXPIRED_DATETIME is null
    IF :NEW.PURCHASED_DATETIME IS NOT NULL 
       AND :NEW.PACKAGE_STATUS = 'Purchased'
       AND :NEW.EXPIRED_DATETIME IS NULL THEN
        SELECT PACKAGE_VALIDITY INTO v_validity_days
        FROM HOSPITAL_PACKAGE
        WHERE PACKAGE_ID = :NEW.PACKAGE_ID;

        -- Calculate the EXPIRED_DATETIME based on the latest PURCHASED_DATETIME
        :NEW.EXPIRED_DATETIME := :NEW.PURCHASED_DATETIME + v_validity_days;
    END IF;
END;

